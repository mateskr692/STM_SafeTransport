void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
	switch(GPIO_Pin)
	{
		//Button press to change display screen
		case BTN_MODE_Pin:
		{
			SetToChangeDisplayMode();
			break;
		}

		//Button press to calibrate data
		case BTN_ADJUST_Pin:
		{
			SetToCalibrate();
			break;
		}

		default: break;
	}
}

void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	//get the new readings from sensors
	if(htim->Instance == TIM4)
	{
		//reading the data should be consistant so we need to do it here instead of setting flag
		ProcessValues();
	}

	//set flag to update display
	else if( htim->Instance == TIM3)
	{
		SetToUpdateDisplay();
		//HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
	}

	//set flag to save data to SD card
	else if( htim->Instance == TIM8)
	{
		SetToSaveData();
		HAL_GPIO_TogglePin(LED_GPIO_Port, LED_Pin);
	}

	else {}
}
